#include <iostream>
using namespace std;

int main(){
	//Increment Decrement
	int x, y;
	
	//Tambah dan Kurang Sama, Beda Oprator Saja
	
	//Increment "+" / Decrement "-" 
	//Post Increment Sering digunakan
	//Nilai Asli Lalu di Tambah
	x = 1;
	cout << x << endl;
	//"x += 1;" Jika Hanya Ingin Menambahkan Angka Yang diinginkan
	x++;
	cout << x << endl;
	cout << "===================================" << endl;
	
	//Pre Increment
	y = 1;
	cout << y << endl;
	++y;
	cout << y << endl;

}
